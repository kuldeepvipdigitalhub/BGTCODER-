<h1> Introduction Of CSS Comment </h1>
    <P> लगभग हर Web Designing Language में Comment Add करने की सुविधा होती हैं.</P>
        <P> आप PHP, Java, JavaScript और HTML आदि भाषाओं में Comment Write कर सकते हैं.</P>
    <p>CSS Comment भी अन्य भाषाओं के Comment की भांती Browser द्वारा Ignore किये जाते हैं. </P>
        <P>Comments को Visitors नही देख सकते हैं. इन्हे केवल Web Masters ही देख पाते हैं.</p>
<p>Comments में Users के लिए कोई जानकारी नहीं लिखि जाती हैं. Comments के द्वारा Web Masters अपने लिए Notes लिखते हैं.</p>
<h1> Benifits Of CSS Comment </h1>
<p>1. Comment से CSS Code को Easily Explain किया जा सकता हैं.</p>
  <P> 2. Comment एक Stylesheet को पठनीय (Readable) और समझने योग्य (Understandable) बनाते हैं.</P>
  <P>3. Web Masters भविष्य के लिए Notes बनाते हैं. ताकि बाद में CSS को आसानी से Maintenance किया जा सके.</P>
    <P>4. Comment के द्वारा एक Stylesheet को अलग-अलग Sections में बांटा जा सकता हैं.</P>
    <h1>Way Of Writing Comments In CSS</h1>
    <p>Stylesheet में Code Add करना बहुत ही आसान हैं. CSS Comment के लिए Opening Tag और Closing Tag को सही Use करना आना चाहिए</p>
    <p>1. Forward Slash (/) और Asterisk (*) से Comment Open होता हैं. इस तरह – /*</p>
    <p>2. और Asterisk (*) तथा Forward Slash (/) के साथ Comment Close होता हैं. इस तरह */</p>
    <h1>Types Of Comments In CSS</h1>
    <P>1. Single Line Comment </P>
    <p>2. Multi Line Comments</p>
    <h3>Single Line Comments :-</h3>
    <pre>Single Line Comment में आप बस एक Line का Comment Content लिखते हैं. इस तरह Add किया जाता हैं.

        /* This CSS Property Make Paragraph Text Orange */
        
        p {
        color:orange;
        }
        <h3>Multi Line Comment :-</h3>
Multi Line Comment में आप एक से ज्यादा Line का Comment Content लिखते हैं. इस तरह Add किया जाता हैं.

/* This CSS property make paragraph text orange.
You can change this properety.
And write a new properety.*/p {
color:orange;
}
 
