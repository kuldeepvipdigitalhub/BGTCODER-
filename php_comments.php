    <h1> Comments in PHP </h1>    
  <pre>
     Comments program में line of code होता है जो कि execute नहीं होता है,
      जिससे कि जब दुबारा हम उस program पर काम करे तो हर step हमें समझ आ सके कि हमने इसमें किया क्या था। 
      Comments single Line भी हो सकते है और Muliline भी हो सकते हैं। PHP में Comments दो तरह के होते है।
     <h2>PHP में Comments दो तरह के होते है:-</h2>
    <h5> 1-Single line comments</h5>
