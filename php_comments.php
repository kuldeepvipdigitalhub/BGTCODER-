    <h3>Why Should We Use Comments IN PHP:-</h3>
    <pre>
      1- किसी भी project पर काम करने के बाद हमें जरूरत के हिसाब से समय के साथ उसमे changes करने पढ़ ही जाते हैं तो जब हम update करें तो हमें proper पता चले कि हमने किया क्या था।

      2- या जब कोई दूसरा programmer हमारे द्वारा develop किये गए project को देखे या update करे तो उसे भी पता चले।

      3- Comment लिखना आपकी Coding Standardization को भी बताता है।

      4- कमेंट लिखने से Coding Readability भी बढ़ती है।
    </pre>
