<h1>Features Of Python</h1>
<h4> 1.Easy Programming Language<h4>

<P>सभी बाकी programming language में अगर कोई सीखने और उपयोग करने में आसान है, तो वह पाइथन है. दूसरी भाषाओ के विपरीत पाइथन में code करना आसान है. थोड़े बहुत प्रयास के साथ python syntax सीखे जा सकते है. high level programming होने के बावजूद भी python code आसान अंग्रेजी भाषा मे होते है जिन्हें समझना (understand) और सीखना (learn) आसान है. इस कारण आप इसे programmer friendly भी कह सकते है.</P>

<h4> 2.Interpreted Language</h4>

<p>Python को छोड़कर दूसरी programming language को चलाने के लिये हमें इसे संकलित (compile)करने की आवश्यकता होती है. परन्तु python के मामले में python code बिना संकलित किये चलाये जा सकते है. यहां interpreted का अर्थ है, source code को line by line निष्पादित किया गया है.</p>

<h4> 3.Expressive Language</h4>

<p>जब Expressive शब्द का उपयोग होता है, तो इसका अर्थ है, understandable और readable. पाइथन एक ऐसी प्रोग्रामिंग भाषा है, जिसे पढ़ना और समझना बहुत आसान है. कई ऐसे program है, जो बाकी programming language में नही किये जा सकते परन्तु python में किये जाते है.</p>

<h4> 4.Cross- Platform Language</h4>

<p>यदि हम python code को किसी एक operating system (Window, Mac, Android, Linux) के लिए लिखते है, तो आपको इसे किसी दूसरे ऑपरेटिंग सिस्टम में चलाने के लिये कोई बदलाव नही करने होंगे. इसका मतलब है python language सभी platform को support करती है. किसी भी प्रोग्रामिंग भाषा को सीखने से पूर्व आपको देखना होगा क्या वह portable language है.</p>

<h4> 5.Open Source</h4>

<p>पाइथन open source है, इसका अर्थ python source code पूरी दुनिया के लिए उपलब्ध है. हम इसे आसानी से download, change, use और distribute कर सकते है. इसके साथ ही python एक free language है, जिसके tools का उपयोग आप आसानी से कर सकते है.</p>

<h4> 6.Embedded</h4>

<p>पाइथन पूरी तरह से embedded है, अर्थात इसके source code में अन्य programming language के code डाले जा सकते है और अन्य भाषाओं के source code में python code डाला जा सकता है. यह हमें दूसरी भाषाओ की scripting क्षमताओं को हमारे program में एकीकृत करने की अनुमति देता है.</p>

<h4> 7.Large Standard Library</h4>

<p>जब हम python download करते है, तो इसके साथ हमे code की large library भी मुहैया होती है. जिसके कारण आपको हर एक चीज के लिए अपना कोड लिखना नही पड़ता है. यह हमें rapid application development के लिये module और functions का स्मृद्ध सेट प्रदान करता है.</p>

<h4> 8.Extension</h4>

<p>जरूरत पढ़ने पर हम python code को C++ जैसी दूसरी भाषाओं में लिख सकते है. यह पाइथन को एक extensible language बनाता है. इस कारण हम python को अन्य भाषाओं में बड़ा सकते है.</p>

<h4> 9.GUI Programming Support</h4>

<p>python के उपयोग से Graphical user interface (GUI) बनाया जा सकता है. GUI, user interface का रूप है, जो user को command line के माध्यम से केवल text के बजाय electronic device के साथ बातचीत करने के लिए icon या अन्य visual indicators का उपयोग करता है.</p>
