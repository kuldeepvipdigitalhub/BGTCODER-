<h1>python Introduction</h1>
<p>Python एक Object-oriented, High level programming language है, 
   जिसका इस्तेमाल Website building, App development, Machine learning, Data analysis, Web scraping और Natural language processing जैसे कार्यो में किया जाता है.
   पाइथन को general purpose programming language भी कहा जाता है. इसकी शुरूआत 1980 के दशक में हुई थी.</p>
<p>Python language की स्पष्ट syntax और readability के कारण यह आज दुनिया की सबसे popular programming language बन चुकी है.
   पाइथन dynamic typing और dynamic binding जैसे विकल्पों की सुविधा देता है.
   इस कारण Rapid Application development के क्षेत्र में इसका इस्तेमाल बखूबी किया जाता है.</p>
<p>python एक interpreted language भी है, इसका अर्थ हुआ पाइथन में लिखे गये program को चलाने से पहले compiled करने की जरूरत नही होती है. 
   पाइथन भाषा modules और packages के उपयोग का समर्थन करती है.
   आसान शब्दों में समझे तो python program को एक modular style में design कर सकते है और इसके code को कई प्रकार के दूसरे project में पुनः उपयोग किया जा सकता है.</p>
<p>अगर आप programming language sसीखना चाहते है, तो python से शुरुवात करना समझदारी भरा कदम है. क्योंकि इसके code बिल्कुल English language की तरह होते है और इन्हें लिखने के लिए किसी भी तरह के curly bracket ({}) की जरूरत नही होती है.</p>
<p> पाइथन एक बहुत ही सरल और बाकी programming languages JavaScript, C, C++, Java, Kotlin इत्यादि से एक कदम आगे है.
      इसके साथ ही यह programmer’s के लिए नए जमाने की सबसे popular language है, 
      क्योंकि इसका उपयोग web development से लेकर software development और Scientific application बनाने तक हर चीज में किया जाता </p>
<p>आज Google, Yahoo, Quora, Pinterest और Spotify जैसी दिग्गज कंपनियां इसका उपयोग करती</p>
<h1>History Of Python</h1>
<p>Python programming language को 1980 के दशक में Guido Van Rossum द्वारा बनाया गया था.
      इसकी शुरुआत National Institute For mathematics and computer science Netherlands में हुई थी.
       python language का अविष्कार ABC programming language से प्रेणा लेकर हुआ था.
        क्योंकि यह exception handling और Amoeba operation system के साथ interface करने में सक्षम थी.</p>
<p>पाइथन के नाम को लेकर कई लोग सवाल करते है, कि एक सांप के नाम का programming language से क्या ताल्लुक. 
     दरअसल पाइथन के नाम की उत्पत्ति एक comedy show के नाम से हुई. 1970 के दशक में BBC Comedy Series द्वारा Monty Python’s Flying Circus नामक एक script प्रकाशित हुई.
      इससे प्रभावित होकर Van Rossum ने python को नाम दिया.</p>
<p>फिलहाल पाइथन को इस वक्त Core development team द्वारा maintain किया जाता है.
      जो python programming language में हर रोज नए update और features जोड़ते है.
       साल 1994 में पाइथन का First version- Python 1.0 लाया गया था. इसके बाद कई version update आते गए.</p>
