<h1>Python Syntex</h1>
<p>पाइथन लैंग्वेज में हमें कर्ली ब्रैकेट { } और सेमीकोलन “ ; ” का उपयोग करने के जरूर नहीं होती मगर इसमें हम indentation का उपयोग कोड को डिफाइन करने के लिए करते है | </p>

<p>indentation का मतलब प्रोग्राम में हम वाइट स्पेसेस का उपयोग कोड को डिफाइन करने के लिए करते है | </p>
<pre>Example -:


def func():  
       statement 1  
       statement 2  
       statement 3
       …………………  
       …………………  
         statement N  
         <Pre>
         	<p>ऊपर दिए गए उदाहरण में जितने भी स्टेटमेंट है वो फंक्शन के राइट साइड है |  आमतौर पर हम indentation को डिफाइन करने के लिए चार white spaces का उपयोग करते है जो बताता है कि कौन सा स्टेटमेंट किस ब्लॉक और फंक्शन का है | </p>
         	<p>पाइथन लैंग्वेज बाकि सभी लैंग्वेज से थोड़ा भिन्न है इसमें हम कुछ ही लाइन में अपना कोड लिख सकते है | 

जिस कोड को दूसरे लैंग्वेज जैसे Java, C/C++ आदि में लिखने में हमे काफी ज्यादा लाइन में लिखना पड़ता था उसी को हम पाइथन की मदद से कुछ ही लाइन में लिख सकते है |</p>
